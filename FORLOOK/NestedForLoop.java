package FORLOOK;

public class NestedForLoop {

	public static void main(String[] args) {
	 //1 den 4 e kadar sayıları yan yana aralarında bir boşluk bırakarak yazdırın
	//	
	for (int satir = 1; satir <=4; satir++) {
		for (int i =1; i <=4 ; i++) {
			System.out.print(satir*i+" ");
	}
		
		System.out.println("");
		
	}	
		
	}

}
