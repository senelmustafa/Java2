package FORLOOK;

import java.util.Scanner;

public class ForLoop4 {

	public static void main(String[] args) {
		// 10 dan küçük pozitif tam sayı
		// girdiği sayıya göre aşağıdaki şekli yazdırın
		// 1
		// 1 2
		// 1 2 3

		for(int i=1; i<=5; i++) {		

			for(int j=(5-i); j>1; j--) {

				System.out.print(i+j);

			}	

			System.out.println();			

	}
}
}
		 
			 
		 
		 
 
